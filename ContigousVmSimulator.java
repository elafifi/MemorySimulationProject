    private void clear() {
        // remove input table 
        DefaultTableModel outputTable = (DefaultTableModel) jTable2.getModel();
        int rowCount = outputTable.getRowCount();
         //Remove rows one by one from the end of the table
         for (int i = rowCount - 1; i >= 0; i--) {
               outputTable.removeRow(i);
          }
         // remove output table
         DefaultTableModel inputTable = (DefaultTableModel) jTable1.getModel();
         rowCount = inputTable.getRowCount();
         //Remove rows one by one from the end of the table
         for (int i = rowCount - 1; i >= 0; i--) {
               inputTable.removeRow(i);
          }
    }
