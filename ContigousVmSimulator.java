    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int nProcesses = (int)jSpinner1.getValue();
        DefaultTableModel inputTable = (DefaultTableModel)jTable1.getModel();
        DefaultTableModel outputTable = (DefaultTableModel)jTable2.getModel();
        ContigousVM vm = new ContigousVM();
        for(int i = 0;i<nProcesses;i++) {
            int pid = (int)inputTable.getValueAt(i, 0);
            int processSize = (int)inputTable.getValueAt(i, 1);
            Process1 p = new Process1(pid,processSize);
            vm.execute(p);
            int pEnd = p.start + p.size - 1;
            outputTable.addRow(new Object[]{
                pid,
                p.start,
                pEnd, 
                p.isCanceled? 
                        "Canceled": p.status?
                                "Finished": "Waiting"
            });
        }
    }//GEN-LAST:event_jButton2ActionPerformed

